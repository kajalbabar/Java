/* write a java program to print the pattern
'AbAbAbAbAb' with dry run
*/

class Program {
      public static void main(String[] args) {
            //use for loop for printing the pattern
            for(int i=0; i<5; i++) {
                System.out.print("Ab");     //it will print the string for 5 times
            }//..for
            System.out.println();
      }//..main
}//..class
