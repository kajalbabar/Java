class IfElseDemo {
    public static void main(String[] args) {
          int marks = 80;
          //if can work without else
          if(marks > 90)
              System.out.println("bike");

          //------------------------ if else-------------------------
          if(marks > 90)
              System.out.println("bike");
          else
              System.out.println("Nokia ch phone");
          
        }

}
