//switch statment

class SwitchDemo{
          public static void main(String[] args) {
              int var = 10;

              //pass to the switch
              switch(var) {
                    case 1:
                          System.out.println("One");
                          break;
                    case 2:
                          System.out.println("two");
                          break;
                    default:
                          System.out.println("Default statement");
                          break;
              }

          }

}
