class CharDemo {
  public static void main(String[] args) {
          char a = 'k';
          System.out.println("char "+a);      //k
          a = 65;           //65 is considered as ascii value so its relative character i.e. A will be printed
          System.out.println(a);
         //a = 'ka';        //error char can store only one character
         a = 60000;
         System.out.println(a);
         
    }
}
