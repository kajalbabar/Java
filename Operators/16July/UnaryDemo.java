class UnarydDemo {
    public static void main(String[] args) {
            int num  = 10;
            System.out.println(+num);                     //10
            System.out.println(-num);                     //-10
            System.out.println(++num);                    // 11       ==> this will inc value by one
            System.out.println(--num);                    //10        ==> decrement value by one
            
    }
}
